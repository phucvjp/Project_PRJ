    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO d = new DAO();
        HttpSession session = request.getSession();
        if (request.getParameter("pid") != null && request.getParameter("oid") != null) {
            request.setAttribute("pro", d.getP(Integer.parseInt(request.getParameter("pid"))));
            request.setAttribute("pid", Integer.parseInt(request.getParameter("pid")));
            request.setAttribute("oid", Integer.parseInt(request.getParameter("oid")));
            request.getRequestDispatcher("rate.jsp").forward(request, response);
        } else {
            response.sendRedirect("home");
        }
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO d = new DAO();
        HttpSession session = request.getSession();
        d.insertEva((Account) session.getAttribute("account"), Integer.parseInt(request.getParameter("pid")),
                Integer.parseInt(request.getParameter("oid")), request.getParameter("content"), Double.parseDouble(request.getParameter("rate")));
        request.setAttribute("oid", request.getParameter("oid"));
        log("oke");
        request.getRequestDispatcher("orderDetail").forward(request, response);
    }

